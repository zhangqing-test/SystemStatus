package com.system.web.vo;

public class ProcessInfo {
	
	private String pid;


	private String user;

	

	private String startTime;

	

	private String memSize;

	

	private String memUse;

	

	private String memhare;

	

	private String state;

	

	private String cpuTime;

	

	private String name;

	private Double cpu;

	public ProcessInfo() {
		super();
	}



	public ProcessInfo(String pid, String user, String startTime,
			String memSize, String memUse, String memhare, String state,
			String cpuTime, String name,Double cpu) {
		super();
		this.pid = pid;
		this.user = user;
		this.startTime = startTime;
		this.memSize = memSize;
		this.memUse = memUse;
		this.memhare = memhare;
		this.state = state;
		this.cpuTime = cpuTime;
		this.name = name;
		this.cpu=cpu;
	}



	public String getPid() {

		return pid;

	}



	public void setPid(String pid) {

		this.pid = pid;

	}



	public String getUser() {

		return user;

	}



	public void setUser(String user) {

		this.user = user;

	}



	public String getStartTime() {

		return startTime;

	}



	public void setStartTime(String startTime) {

		this.startTime = startTime;

	}



	public String getMemSize() {

		return memSize;

	}



	public void setMemSize(String memSize) {

		this.memSize = memSize;

	}



	public String getMemUse() {

		return memUse;

	}



	public void setMemUse(String memUse) {

		this.memUse = memUse;

	}



	public String getMemhare() {

		return memhare;

	}



	public void setMemhare(String memhare) {

		this.memhare = memhare;

	}



	public String getState() {

		return state;

	}



	public void setState(String state) {

		this.state = state;

	}



	public String getCpuTime() {

		return cpuTime;

	}



	public void setCpuTime(String cpuTime) {

		this.cpuTime = cpuTime;

	}



	public String getName() {

		return name;

	}



	public void setName(String name) {

		this.name = name;

	}
	
	


	public Double getCpu() {
		return cpu;
	}



	public void setCpu(Double cpu) {
		this.cpu = cpu;
	}



	@Override
	public String toString() {
		return "ProcessInfo [pid=" + pid + ", user=" + user + ", startTime="
				+ startTime + ", memSize=" + memSize + ", memUse=" + memUse
				+ ", memhare=" + memhare + ", state=" + state + ", cpuTime="
				+ cpuTime + ", name=" + name + ", cpu=" + cpu + "]";
	}



	
	

}
