	public Services createServices(Manager one, Manager two, Manager third) {
		Services services = new Services();
		services.setHardwareId(hardwareId);
		services.setName(name);
		services.setYt(yt);
		services.setJk(jk);
		services.setType(type + 1);
		services.setSpaceName(spaceName);
		services.setCpuLevel(cpuLevel);
		services.setMemoryLevel(memoryLevel);
		services.setSpaceLevel(spaceLevel);
		services.setFirstManager(one.getId());
		services.setSecondManager(two.getId());
		if (null != third) {
			services.setThirdManager(third.getId());
		} else {
			services.setThirdManager(null);
		}
		services.setPort(port);
		services.setRestartBat(restartBat);
		services.setComments(comments);
		services.setRequestId(id);
		services.setStatus(1);
		services.setDatetime(null);
		if (0 == type) {
			// 应用服务器
			services.setCalcType("1,2,3");
		} else if (1 == type) {
			// 数据库服务器忽略内存
			services.setCalcType("1,3");
		}
		services.setMessage(true);
		return services;
	}

